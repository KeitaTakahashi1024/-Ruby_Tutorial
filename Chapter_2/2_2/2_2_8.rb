# 2_2_8 変数の代入と宣言 p.27 ~ p.30

# 変数の宣言と代入例
s = 'Hello'
n = 123 * 2


# スネークケースで宣言する例（一般的に使われる記法）
special_price = 200

# キャメルケースで宣言する例（あまり使わない記法）
specialPrice = 200

# 多重代入
a,b = 1,2 # a => 1, b => 2

# 多重代入（右辺の数が多い場合）
c, d = 10 # c => 10, d => nil

# 多重代入（左辺の数が多い場合）
e,f = 100, 200, 300 # e => 100, f => 200

#同時代入
a = b = 100 # a => 100, b => 100





